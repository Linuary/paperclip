    const currentZh = zhFlat[key];
    // Only translate if current zh value equals en value (untranslated) or is missing
    if (currentZh === enValue || currentZh === undefined) {
      setNestedValue(zhCN, key, value);
      count++;
    } else {
      skipped++;
    }
